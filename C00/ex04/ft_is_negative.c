#include <unistd.h>

void	ft_is_negative(int n)
{
	char	c;

	c = 'P';
	if (n >= 0)
	{
		write(1, &c, 1);
	}
	else
	{
		c = 'N';
		write(1, &c, 1);
	}	
}
